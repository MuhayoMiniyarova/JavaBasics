package class3;

public class E5RelationalOperators {
    public static void main(String[] args) {
        System.out.println(10>15); //is 10 grater than 15=false
        System.out.println(10<15);  //is 10 smaller than 15=true
        System.out.println(10==15); // is 10 equal to 15=false
        System.out.println(10!=15); // is 10 not equal to 15=true
        System.out.println(10>=15); // is 10 grater than or equal to 15=false
        System.out.println(10<=15); // is less than or equal to 15= true or false




    }
}
